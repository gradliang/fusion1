typedef struct
{
    char Format[4];
    char Description[24];
    unsigned char GeneralHeight;
	unsigned char GeneralWidth;
	unsigned char BitPerPixel;
    unsigned char Align;
} ST_IBF_FILE_HEADER; //  total 32(0x20) bytes

static ST_IBF_FILE_HEADER FileHeader;
BYTE *pFileHeader = (BYTE *) &FileHeader;

static  STREAM* handle=NULL;
//BYTE inFile[32]="Guobi";
BYTE inFile[32]="font";


void FileBitmapInit(char *file, WORD line)
{
    MP_DEBUG("%s()", __FUNCTION__);
    if(handle != NULL)
    {
        MP_ALERT("%s.ibf had been opened, handle != NULL - skip", inFile);
        return;
    }
    DRIVE *srtDrv = DriveGet(DriveCurIdGet());
    //mpDebugPrint("DriveCurIdGet()=%d", DriveCurIdGet());
    int rc = FileSearch(srtDrv, inFile, "ibf", E_FILE_TYPE);
    if(rc == FS_SUCCEED)
    {
        handle = FileOpen(srtDrv);
        MP_ALERT("%s.ibf found, handle = 0x%X, in file:%s, line:%d", inFile, handle, file, line);
    }
    else
        MP_ALERT("%s.ibf NOT found! in file:%s, line:%d", inFile, file, line);

    FileRead(handle, pFileHeader, sizeof(ST_IBF_FILE_HEADER));
	//mpDebugPrint("FileHeader.GeneralHeight  = 0x%x(%d)", FileHeader.GeneralHeight, FileHeader.GeneralHeight);
	//mpDebugPrint("FileHeader.GeneralWidth   = %d", FileHeader.GeneralWidth);
	//mpDebugPrint("FileHeader.BitPerPixel    = %d", FileHeader.BitPerPixel);
	//mpDebugPrint("FileHeader.Align          = %d", FileHeader.Align);

    return;
}


U08 FileBitmapGet(U16 code, U08 *target)
{
    MP_DEBUG("%s()", __FUNCTION__);
    DWORD code_offset, page_format, page_offset;
    WORD  glyph_offset;

    // compute page_offset
    //mpDebugPrint("code = 0x%X", code);
    code_offset = ((code >> 8) << 2) + sizeof(ST_IBF_FILE_HEADER);
    //mpDebugPrint("code_offset = 0x%X", code_offset);
    Fseek(handle, code_offset, SEEK_SET);
    //mpDebugPrint("********");
    FileRead(handle, (U08 *)&page_offset, 4);
    //mpDebugPrint("page_offset = 0x%X", page_offset);

    // compute glyph_offset
    //mpDebugPrint("(code & 0xff) * 2 = 0x%X", (code & 0xff) * 2);
	//mpDebugPrint("page_offset + (code & 0xff) * 2 = 0x%X", page_offset+(code & 0xff) * 2);
    Fseek(handle, page_offset + (code & 0xff) * 2, SEEK_SET);
    FileRead(handle, (U08 *)&glyph_offset, 2);
	//mpDebugPrint("glyph_offset = 0x%X", glyph_offset);
	glyph_offset = glyph_offset * 4;
    //mpDebugPrint("glyph_offset = 0x%X", glyph_offset);
    if(glyph_offset < 512) // 512 = 0x200 = 0x80 * 4 - not found!
        return 0;   // no glyph found

    //mpDebugPrint("page_offset + glyph_offset = 0x%X", page_offset + glyph_offset);
    Fseek(handle, page_offset + glyph_offset, SEEK_SET);
    FileRead(handle, target, 128);

    return 1;   // glyph found
}

WORD xpgOSDUniStringOut(WORD *unistr, WORD x, WORD y, BYTE bColor, WORD width_max)
{
    //mpDebugPrint("xpgOSDUniStringOut(unistr = %s, x=%d, y=%d, width_max=%d)", unistr, x, y, width_max);
    if(handle == NULL)
    {
        MP_ALERT("%s.ibf NOT FOUND, handle == NULL - skip", inFile);
        return;
    }
    WORD unicode;
    WORD w, w_sum=0; // return font char width
    unicode=*unistr++;
    while(unicode != 0x0000)
    {
        MP_DEBUG("xpgOSDStringOut(), unicode=0x%X", unicode);
        w=OSDTextOut(unicode, x, y, bColor, width_max - w_sum);
        w_sum += w;
        if(w_sum > width_max)
            break;
        unicode=*unistr++;
        x+=w;
        //x+=2; // 2=char gap
    }
    return w_sum;
}

BYTE OSDTextOut(WORD code, WORD x, WORD y, BYTE bColor, WORD width_space)
{
        MP_DEBUG("%s() code = 0x%X, x=%d, y=%d, width_space=%d", __FUNCTION__, code, x, y, width_space);
        U08 mask;
        BYTE indent, width, height;
        BYTE bitmapData[128];
        BYTE *bitmap = &bitmapData[0];
        memset(bitmap, 0, sizeof(bitmap));
        mask = FileBitmapGet(code, bitmap);
        if(mask <= 0)
        {
            MP_ALERT("code(0x%X) not supported", code);
            return 0;
        }
        else
            ; //MP_DEBUG("mask = %d, FileBitmapGet() succeed!", mask);

        indent = *(bitmap + 0);
        width  = *(bitmap + 1);
        height = *(bitmap + 2);
        //mpDebugPrint("indent = %d", indent);
        //mpDebugPrint("width  = %d", width);
        //mpDebugPrint("height = %d", height);
        if(width > width_space)
        {
            //mpDebugPrint("width > width_space, no space to draw !");
            return 0;
        }

        ST_OSDWIN *OSDWin=Idu_GetOsdWin();
        BYTE *pixel;
        pixel = (BYTE *) OSDWin->pdwStart + y * (OSDWin->wWidth >> OSD_BIT_OFFSET) + (x >> OSD_BIT_OFFSET);

        U08 *pointer, byte, index, even;
        if(FileHeader.Align == 0) // compact
        {
            pointer = bitmap + 3;
            byte = *pointer;
            mask = 0x80;
            even=0;
            while(height)
            {
                for(index = 0; index < width; index ++)
                {
                    if(!mask)       // read one work from font and reset the bit mask
                    {
                        pointer ++;     byte = *pointer;
                        mask = 0x80;
                    }

                    // get the bit status, 1 to draw, 0 to leave or draw backcolor
                    // only for OSD_BIT_WIDTH =8, OSD_BIT_WIDTH = 4  TODO
                    if(byte & mask)
                    {
                        *pixel = bColor;
                        pixel ++;
                    }
                    else
                    {
                        *pixel = 0; // background color
                        *pixel ++;
                    }

                    mask >>= 1;
                }

                height --;
                pixel = pixel + (OSDWin->wWidth >> OSD_BIT_OFFSET) - width;
            }
    	}
    	else if(FileHeader.Align == 1) // 1 byte align
    	{
    		//WORD byteCountWidth = (width * FileHeader.BitPerPixel + 7) / 8;
    		WORD byteCountWidth = (width + 7) / 8;
    		BYTE i,j;
    	    //mpDebugPrint("byteCountWidth = %d\n", byteCountWidth);
    	    for(i=0; i<FileHeader.GeneralHeight; i++)
    		{
    			for(j=0; j < byteCountWidth; j++)
    			{
    				BYTE ch = *(bitmap + 3 + i*byteCountWidth + j); // 3 = indent + width + height,
    				if(ch & 0x80) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x40) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x20) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x10) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x08) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x04) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x02) *pixel = bColor; else *pixel = 0;
    				if(ch & 0x01) *pixel = bColor; else *pixel = 0;
    			}
    		}
    	} // else if(FileHeader.BitPerPixel == 2)
    return width;
    //MP_DEBUG("%s() ---->Exit", __FUNCTION__);
}

void FileBitmapClose()
{
    MP_DEBUG("%s()", __FUNCTION__);
    fclose(handle);
}

