///
///@ingroup xpgFuncEntry
///@brief   Draw 48x48 char(0..9,:) Font for Calendar
///
///@param   ch - ch ascii code
///@param   x- left
///@param   y- top
///@param   bColor - color index of OSD
///
BYTE xpgOsdDrawFont48(BYTE ch, WORD x, WORD y, BYTE bColor)
{
    BYTE temp[2048];
    BYTE *pData;
    pData = (BYTE *)fontAddress[ch-0x20];
    BYTE wMax, hMax;
    int i,w, h, wByte;
    wMax = pData[0]; // width
    wByte = (wMax + 7)/8;
    hMax = pData[1]; // height
    //mpDebugPrint("xpgOsdDrawFont48(), ch=0x%X, wMax=%d, wByte=%d, hMax=%d", ch, wMax, wByte, hMax);
    for(h=0; h<hMax; h++) {
        for(w=0; w<wByte; w++) {
            for(i=0; i<8; i++) {
                if(pData[2+h*wByte+w] & byteMask[i])
                    temp[h*wByte*8+w*8+i]=bColor;
                else temp[h*wByte*8+w*8+i]=0;
            }
        }
    }
  //  Idu_OsdPaintData(x, y, wByte*8, hMax, &temp[0]);
    return wMax;
}

///
///@ingroup xpgFuncEntry
///@brief   Draw 48x48 string Font for Calendar
///
///@param   str - pointer of string
///@param   x- left
///@param   y- top
///@param   bColor - color index of OSD
///
void xpgOsdStringFont48(BYTE *str, WORD x, WORD y, BYTE bColor)
{
    BYTE ch=*str++;
    BYTE w;
    while(ch>=0x20 && ch<=0x5A)
    {
        MP_DEBUG("xpgOsdStringFont48(), ch=0x%X", ch);
        w=xpgOsdDrawFont48(ch, x, y, bColor);
        ch=*str++;
        x+=w;
        x+=2; // 2=char gap
    }
}

