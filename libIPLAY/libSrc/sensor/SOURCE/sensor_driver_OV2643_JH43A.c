static BYTE CurVideoFormat = NO_SIGNAL;
void pal_ntsc_check(void)
{
	BYTE bNewVideoFormat;

   // MP_DEBUG("%s", __FUNCTION__);
	//Ui_TimerProcAdd(100,pal_ntsc_check);
	if (!CVBS_Singal_Detect())
	{
		if (CurVideoFormat)
		{
		    MP_DEBUG("%s,no signal", __FUNCTION__);
			CurVideoFormat=NO_SIGNAL;
		}
		return ;//NO_SIGNAL;
	}

	bNewVideoFormat=Pal_Ntsc_check_Is_Pal();
	
	if (CurVideoFormat!=bNewVideoFormat)
	{
		TurnOffBackLight();
#if T530_VIDEO_FORMAT_DETECT_ON
		if (g_bNonXpgStatus==NON_XPG_MODE_AVIN && bNewVideoFormat && !CurVideoFormat)
		{
			Set_Display_flag(1);
			API_Open_Write_Into_EncBuf();
		}
#endif
		Sensor_GPIO_PreReset();
		Local_Sensor_GPIO_Reset();
		MP_DEBUG("%s, signal %d->%d", __FUNCTION__,CurVideoFormat,bNewVideoFormat);
		CurVideoFormat=bNewVideoFormat;
		bT530InitFinish=0;
		Initial_T530();
		
		Idu_DisplayOff();
		Idu_PanelSystemChange(CurVideoFormat);
		Idu_DisplayON();
		TurnOnBackLight();
	}

}

