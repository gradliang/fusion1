BYTE DataInRage(unsigned short sData, unsigned short tData, unsigned char rData)
{
	if((sData<(tData+rData))&&(sData>(tData-rData)))
		return 1;
	else
		return 0;
}

BYTE CVBS_Singal_Detect(void)
{
    MP_DEBUG("__%s__ %x", __FUNCTION__,I2CM_GetRegister(TWIC_P2,VD_Status_P2));
	if(I2CM_GetRegister(TWIC_P2,VD_Status_P2)&0x07 !=0x06) 
	{
		IODelay(2);
		if(I2CM_GetRegister(TWIC_P2,VD_Status_P2)&0x07 !=0x06)	  
		{
			return 0;
		}
	}
	return 1;
}

